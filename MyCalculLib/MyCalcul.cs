using System;

namespace MyCalculLib
{
    public class MyCalcul
    {
        public double Sum(double x, double y)
        {
            return (x + y);
        }
        public double Min(double x, double y)
        {
            return (x - y);
        }
        public double Umn(double x, double y)
        {
            return (x * y);
        }
        public double Del(double x, double y)
        {
            return (x / y);
        }
    }
}
